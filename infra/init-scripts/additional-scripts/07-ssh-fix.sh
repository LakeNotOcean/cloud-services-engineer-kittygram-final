# Создание усиленной конфигурации SSH с акцентом на безопасность
cat >> /etc/ssh/sshd_config << EOF

# Запрещаем прямой вход под root для предотвращения атак на привилегированный аккаунт
PermitRootLogin no

# Отключаем аутентификацию по паролю, используем только SSH-ключи
# Это значительно повышает безопасность, так как ключи сложнее взломать
PasswordAuthentication no
ChallengeResponseAuthentication no

# Ограничиваем количество попыток входа до трёх
# Помогает предотвратить брутфорс-атаки
MaxAuthTries 3

# Настраиваем автоматическое закрытие неактивных сессий
# Таймаут 300 секунд (5 минут), максимум 2 попытки проверки соединения
ClientAliveInterval 300
ClientAliveCountMax 2

# Используем только современные криптографические алгоритмы
# curve25519 - самый современный и безопасный алгоритм обмена ключами
# nistp521/384/256 - эллиптические кривые разной степени защиты
KexAlgorithms curve25519-sha256@libssh.org,ecdh-sha2-nistp521,ecdh-sha2-nistp384,ecdh-sha2-nistp256

# Задаём список разрешённых шифров, от более к менее предпочтительным
# chacha20-poly1305 - современный высокопроизводительный шифр
# aes256-gcm/aes128-gcm - надёжные шифры с аутентификацией
Ciphers chacha20-poly1305@openssh.com,aes256-gcm@openssh.com,aes128-gcm@openssh.com

# Настраиваем алгоритмы проверки целостности сообщений (MAC)
# Используем только современные алгоритмы с шифрованием тегов (etm)
MACs hmac-sha2-512-etm@openssh.com,hmac-sha2-256-etm@openssh.com

# Используем только SSHv2, так как SSHv1 имеет критические уязвимости
Protocol 2
EOF

# Перезапуск SSH-сервера
systemctl restart ssh || systemctl restart sshd