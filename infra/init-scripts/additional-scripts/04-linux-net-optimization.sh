# Этот конфигурационный файл содержит настройки ядра Linux для оптимизации сетевой подсистемы
# Изменения записываются в /etc/sysctl.conf для их сохранения после перезагрузки
cat >> /etc/sysctl.conf << EOF

# == НАСТРОЙКА СЕТЕВЫХ БУФЕРОВ ==
# Увеличиваем максимальный размер буфера приёма до 16 Мб
# Это улучшает производительность при получении больших объёмов данных
net.core.rmem_max = 16777216

# Увеличиваем максимальный размер буфера отправки до 16 Мб
# Позволяет эффективно отправлять большие объёмы данных без задержек
net.core.wmem_max = 16777216

# Настраиваем динамические буферы TCP для приёма данных
# Три значения: минимальный, начальный и максимальный размер буфера
# 4096 - минимальный размер для поддержания базовой производительности
# 87380 - начальный размер, оптимальный для большинства соединений
# 16777216 - максимальный размер для высоконагруженных соединений
net.ipv4.tcp_rmem = 4096 87380 16777216

# Аналогичная настройка для буфера отправки TCP
# Значения подобраны для оптимального баланса производительности
net.ipv4.tcp_wmem = 4096 65536 16777216

# == ОПТИМИЗАЦИЯ TCP ==
# Включаем TCP Fast Open (TFO) - технологию ускорения повторных соединений
# Значение 3 из настройки TCP (16777216) активирует TFO как для входящих,
# так и для исходящих соединений
# Это сокращает время установки соединения на один RTT (round-trip time)
net.ipv4.tcp_fastopen = 3

# == НАСТРОЙКА TCP KEEPALIVE ==
# Эти параметры определяют, как быстро система обнаружит разрыв соединения
# Время в секундах до отправки первого keepalive-пакета
# 120 секунд - агрессивная настройка для быстрого обнаружения проблем
net.ipv4.tcp_keepalive_time = 120

# Интервал между повторными keepalive-пакетами, если нет ответа
# 60 секунд между попытками - баланс между скоростью обнаружения и нагрузкой
net.ipv4.tcp_keepalive_intvl = 60

# Количество попыток отправки keepalive перед закрытием соединения
# 4 попытки дают достаточно возможностей для восстановления связи
net.ipv4.tcp_keepalive_probes = 4

# Уменьшаем количество попыток ответа на входящий пакет
# 2 попытки - компромисс между надёжностью и защитой от атак
net.ipv4.tcp_synack_retries = 2
EOF

# Применяем все настройки немедленно без перезагрузки системы
# команда sysctl -p загружает настройки из файла /etc/sysctl.conf
sysctl -p